/*First Part - Table Creating*/

CREATE TABLE EMPLOYEE 
   (	EMPLOYEEID VARCHAR2(5) NOT NULL, 
	EMPLOYEEFIRSTNAME VARCHAR2(25) NOT NULL, 
	EMPLOYEELASTNAME VARCHAR2(25) NOT NULL, 
  EMPLOYEEPHONE NUMBER(10), 
	EMPLOYEESTREET VARCHAR2(50), 
	EMPLOYEECITY VARCHAR2(25), 
	EMPLOYEESTATE CHAR(2), 
	EMPLOYEEZIP_CODE VARCHAR2(9), 
	EMPLOYEEEMAIL VARCHAR2(50), 
	EMPLOYEEDOB DATE, 
	EMPLOYEEHIROFEDATE DATE DEFAULT SYSDATE NOT NULL, 
	EMPLOYEESSN NUMBER(9) NOT NULL, 
	EMPLOYEESTATUS CHAR(2), 
	EMPMANAGERID VARCHAR2(5), 
	 CONSTRAINT "EMPLOYEE_PK" PRIMARY KEY ("EMPLOYEEID"));
   
   
CREATE TABLE STORE 
   (	STOREID VARCHAR2(5) NOT NULL, 
	STORESTREET VARCHAR2(50), 
	STORECITY VARCHAR2(25), 
	STORESTATE CHAR(2), 
	STOREZIP_CODE VARCHAR2(9), 
	 CONSTRAINT "STORE_PK" PRIMARY KEY ("STOREID"));
   

CREATE TABLE TAX 
   (	STATE CHAR(2) NOT NULL, 
	TAX_RATE NUMBER(2,2), 
	 CONSTRAINT TAX_PK PRIMARY KEY (STATE));


CREATE TABLE HOLD
   (	STOREID VARCHAR2(5) NOT NULL, 
	PRODUCTID VARCHAR2(5) NOT NULL, 
	QTYHELD NUMBER(4), 
	 CONSTRAINT HOLD_PK PRIMARY KEY (STOREID, PRODUCTID));


CREATE TABLE REVIEW 
   (	REVIEWID VARCHAR2(5) NOT NULL, 
	REVIEWDATE DATE DEFAULT SYSDATE NOT NULL, 
	REVIEWRATING CHAR(1) NOT NULL, 
	REVIEWDESCRIPTION VARCHAR2(250), 
	PRODUCTID VARCHAR2(5) NOT NULL, 
	CUSTOMERID VARCHAR2(5) NOT NULL, 
	 CONSTRAINT REVIEW_PK PRIMARY KEY (REVIEWID));


CREATE TABLE CUSTOMER 
   (	CUSTOMERID VARCHAR2(5) NOT NULL, 
	CUSTOMERFIRSTNAME VARCHAR2(25) NOT NULL, 
	CUSTOMERLASTNAME VARCHAR2(25) NOT NULL, 
	CUSTOMERPHONE NUMBER(10), 
	CUSTOMERSTREET VARCHAR2(50), 
	CUSTOMERCITY VARCHAR2(25), 
	CUSTOMERSTATE CHAR(2), 
	CUSTOMERZIP_CODE VARCHAR2(9), 
	CUSTOMEREMAIL VARCHAR2(50), 
	 CONSTRAINT CUSTOMER_PK PRIMARY KEY (CUSTOMERID));


CREATE TABLE MEMBER
   (	MEMBERID VARCHAR2(5) NOT NULL, 
	MEMBERDRIVERLICENSENO VARCHAR2(9), 
	ENROLLOFDATE DATE DEFAULT SYSDATE, 
	 CONSTRAINT MEMBER_PK PRIMARY KEY (MEMBERID));


CREATE TABLE NUTRITION 
   (	PRODUCTID VARCHAR2(5), 
	TTLCALORIES NUMBER(4), 
	FATCALORIES NUMBER(4), 
	TTLFAT NUMBER(4), 
	SATFAT NUMBER(4), 
	TRANSFAT NUMBER(4), 
	CHOLESTEROL NUMBER(4), 
	SODIUM NUMBER(4), 
	TTLCARBOHYDRATES NUMBER(4), 
	DIETARYFIBER NUMBER(4), 
	SUGARS NUMBER(4), 
	PROTEIN NUMBER(4), 
  PRIMARY KEY (PRODUCTID));
  

CREATE TABLE PRODUCT
   (	PRODUCTID VARCHAR2(5) NOT NULL, 
	PRODUCTNAME VARCHAR2(25), 
  PRODUCTDESCRIPTION VARCHAR2(50);

	


CREATE TABLE SUPPLIER
   (	SUPPLIERID VARCHAR2(2) NOT NULL, 
	SUPPLIERNAME VARCHAR2(20) NOT NULL, 
  SUPPLIERPHONE NUMBER(10), 
	SUPPLIERSTREET VARCHAR2(50), 
	SUPPLIERCITY VARCHAR2(20), 
	SUPPLIERSTATE CHAR(2), 
	SUPPLIERZIP_CODE VARCHAR2(9), 
	EMPLOYEEEMAIL VARCHAR2(50), 
	 CONSTRAINT SUPPLIER_PK PRIMARY KEY (SUPPLIERID));
   

CREATE TABLE DEPARTMENT
   (	"DEPARTMENTID" VARCHAR2(5) NOT NULL, 
	"DEPARTMENTNAME" VARCHAR2(30), 
	 CONSTRAINT "DEPT_PK" PRIMARY KEY ("DEPARTMENTID"));



